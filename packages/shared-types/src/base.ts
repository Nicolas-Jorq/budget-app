/**
 * @fileoverview Base types shared across all modules.
 * These types provide the foundation for the multi-module architecture.
 */

// ==========================================
// Module System Types
// ==========================================

export type ModuleType = 'FINANCE' | 'HEALTH' | 'TASKS' | 'LIFE_GOALS'

export interface UserModule {
  id: string
  userId: string
  module: ModuleType
  enabledAt: string
  settings?: Record<string, unknown>
}

export interface ObsidianSync {
  id: string
  userId: string
  vaultPath?: string
  lastExport?: string
  lastImport?: string
  syncSettings?: ObsidianSyncSettings
  isEnabled: boolean
  createdAt: string
  updatedAt: string
}

export interface ObsidianSyncSettings {
  syncFinance?: boolean
  syncHealth?: boolean
  syncTasks?: boolean
  syncLifeGoals?: boolean
  exportFolder?: string
  importFolder?: string
  frequency?: 'manual' | 'hourly' | 'daily'
}

// ==========================================
// Base Entity Types
// ==========================================

export interface BaseEntity {
  id: string
  createdAt: string
  updatedAt: string
}

export interface UserOwnedEntity extends BaseEntity {
  userId: string
}

// ==========================================
// User Types
// ==========================================

export interface User {
  id: string
  email: string
  name: string
  createdAt: string
  updatedAt: string
}

export interface UserWithModules extends User {
  modules: UserModule[]
  obsidianSync?: ObsidianSync
}

// ==========================================
// API Response Types
// ==========================================

export interface ApiResponse<T> {
  data?: T
  error?: string
  message?: string
}

export interface PaginatedResponse<T> {
  data: T[]
  total: number
  page: number
  pageSize: number
  hasMore: boolean
}

